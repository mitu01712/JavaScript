//Arithmatic Operators
// Addition:'+'
// Subtraction:'-'
// Multiplicaation:'*'
// Division:'/'
// Modulus:'%'
// Increment:'++'
// Decrement:'--'
console.clear();
var num1 = 10;
var num2 = 15;
var num3 = 20;

// Addition:'+'
console.log(num1 + num2)
console.log(num1 + num2 + num3)

// Subtraction:'-'
console.log(num2 - num1)
console.log(num3 - num2 - num1)

// Multiplicaation:'*'
console.log(num1 * num2)

// Division:'/'
console.log(num2/num1)

// Modulus:'%'
console.log(num2 % num1)

//Increment - post and pre
console.log(num1++)
console.log(num1++)
console.log(num1)

//pre
console.log(++num1)
console.log(num1)
//Decrement post and pre
console.log(num1--)
console.log(num1)
//pre
console.log(--num1)
console.log(num1)






